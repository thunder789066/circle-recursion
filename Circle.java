import java.awt.Color;

public class Circle {
	public int centerX, centerY, radius;
	public Color color;
	public Circle(int x, int y, int r, Color c) {
		this.centerX = x;
		this.centerY = y;
		this.radius = r;
		this.color = c;
	}
}